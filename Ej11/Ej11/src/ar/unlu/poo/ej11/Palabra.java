package ar.unlu.poo.ej11;

public class Palabra {
    private Integer lon;
    private String palabra;

    public void setLon(Integer lon) {
        this.lon = lon;
    }

    public void setPalabra(String palabra) {
        this.palabra = palabra;
    }

    public Integer getLon() {
        return lon;
    }

    public String getPalabra() {
        return palabra;
    }
}
